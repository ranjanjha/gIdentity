<h3>Q.I never got my confirmation email!</h3>
<p>Ans:The most common reason for this by far is the use of anti-spam measures. Especially those systems which require the sender of an e-mail to manually verify a message. The second most common reason for not getting e-mails is that the e-mail address entered does not exist, double check those e-mails! Finally, certain emailservices (such as msn and hotmail) seem to be better able to receive our messages to you if you have donotreply@gravatar.com added to your contacts already.</p>

<h3>Q.I changed my information, but I still see my old one on other sites!</h3>
<p>Ans.We cache your information for periods of time, which means that your new information will take effect after the old one expires from the cache. The reason we do this is to be able to serve your (and indeed everybody's) information much more quickly and reliably.</p>

<h3>Q.How do I clear my cache?</h3>
<p>Ans:This is a pretty concise guide on clearing your cache no matter what browser you use: http://kb.iu.edu/data/ahic.html.

Basically your computer (in an attempt to make things faster for you) keeps copies of things (your avatar image, for example) on your computer, and the next time you request one of those things instead of getting it from the internet it gets that local saved copy. in this case you don't want the saved copy, you want the new one from the internet. Thats what clearing your cache does... it deletes all the local copies of things that your browser keeps around.
</p>

